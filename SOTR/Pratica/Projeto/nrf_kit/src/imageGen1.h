void set_img_value(char* img, int x, int y, char value) {
    int idx = x*IMGWIDTH+y;
    *(img+idx) = value;
}

uint8_t* gen_start_image() {
    uint8_t* img = k_malloc(sizeof(start_image));
    bytecpy(img, &start_image, sizeof(start_image));
    return img;
}

uint8_t* gen_first_image() {
    uint8_t* img = k_malloc(sizeof(start_image));
    memcpy(img, &start_image, sizeof(start_image));

    set_img_value(img, 0, 7, 0x00);
    set_img_value(img, 0, 9, 0xFF);

    set_img_value(img, 1, 6, 0x80);

    return img;
}

